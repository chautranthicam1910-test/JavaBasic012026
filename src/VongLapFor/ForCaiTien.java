package VongLapFor;

public class ForCaiTien {
    public static void main(String[] args) {

        //Duyet Mang hoac ArrayList de kiem tra tung phan tu trong mang
        //int array1[] = {12, 23, 44, 56, 78};
        int array1[] = new int[]{12, 23, 44, 56, 78}; //cach thu 2 de khai bao mang

        //Khai bao FOR cai tien de DUYET mang / ArrayList
        for (int tenbien1 : array1){
            System.out.println(tenbien1);
        }
    }
}
